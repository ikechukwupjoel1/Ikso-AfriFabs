// Get fabrics by collection
export const getFabricsByCollection = (collection: string): Fabric[] => {
  return fabrics.filter(f => f.collection === collection);
};

// Get similar fabrics (same collection, different product)
export const getSimilarFabrics = (fabric: Fabric, limit: number = 4): Fabric[] => {
  return fabrics
    .filter(f => f.collection === fabric.collection && f.id !== fabric.id)
    .slice(0, limit);
};

